package com.sczn.wearlauncher.location.bean;

/**
 * Description:
 * Created by Bingo on 2019/1/15.
 */
public class LocationInfo {
    /**
     * 0基站,1wifi,2gps
     */
    private int type;

    /**
     * gps信息
     */
    private double lat;
    private double lng;
    private float accuracy;
    private int gpsCount;       //卫星个数
    private double altitude;    //海拔
    private float bearing;      //方向
    private float speed;        //速度

    /**
     * 基站类型
     * 0:非cdma类型
     * 1:为cdma类型
     */
    private String cdma;
    /**
     * 接入基站信息
     */
    private String bts;
    /**
     * 周边基站信息
     */
    private String nearbts;
    /**
     * 基站个数
     */
    private int btsCount;
    /**
     * 已连接wifi信息
     */
    private String mmac;
    /**
     * wifi列表中信号最好的信息
     */
    private String macs;
    /**
     * wifi个数
     */
    private int wifiCount;

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public double getLat() {
        return lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

    public double getLng() {
        return lng;
    }

    public void setLng(double lng) {
        this.lng = lng;
    }

    public float getAccuracy() {
        return accuracy;
    }

    public void setAccuracy(float accuracy) {
        this.accuracy = accuracy;
    }

    public int getGpsCount() {
        return gpsCount;
    }

    public void setGpsCount(int gpsCount) {
        this.gpsCount = gpsCount;
    }

    public double getAltitude() {
        return altitude;
    }

    public void setAltitude(double altitude) {
        this.altitude = altitude;
    }

    public float getBearing() {
        return bearing;
    }

    public void setBearing(float bearing) {
        this.bearing = bearing;
    }

    public float getSpeed() {
        return speed;
    }

    public void setSpeed(float speed) {
        this.speed = speed;
    }

    public String getCdma() {
        return cdma;
    }

    public void setCdma(String cdma) {
        this.cdma = cdma;
    }

    public String getBts() {
        return bts;
    }

    public void setBts(String bts) {
        this.bts = bts;
    }

    public String getNearbts() {
        return nearbts;
    }

    public void setNearbts(String nearbts) {
        this.nearbts = nearbts;
    }

    public int getBtsCount() {
        return btsCount;
    }

    public void setBtsCount(int btsCount) {
        this.btsCount = btsCount;
    }

    public String getMmac() {
        return mmac;
    }

    public void setMmac(String mmac) {
        this.mmac = mmac;
    }

    public String getMacs() {
        return macs;
    }

    public void setMacs(String macs) {
        this.macs = macs;
    }

    public int getWifiCount() {
        return wifiCount;
    }

    public void setWifiCount(int wifiCount) {
        this.wifiCount = wifiCount;
    }

    @Override
    public String toString() {
        return "LocationInfo{" +
                "type=" + type +
                ", lat=" + lat +
                ", lng=" + lng +
                ", accuracy=" + accuracy +
                ", gpsCount=" + gpsCount +
                ", altitude=" + altitude +
                ", bearing=" + bearing +
                ", speed=" + speed +
                ", cdma='" + cdma + '\'' +
                ", bts='" + bts + '\'' +
                ", nearbts='" + nearbts + '\'' +
                ", btsCount=" + btsCount +
                ", mmac='" + mmac + '\'' +
                ", macs='" + macs + '\'' +
                ", wifiCount=" + wifiCount +
                '}';
    }
}
